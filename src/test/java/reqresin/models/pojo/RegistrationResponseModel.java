package reqresin.models.pojo;

public class RegistrationResponseModel {

    String id, token;

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }

}